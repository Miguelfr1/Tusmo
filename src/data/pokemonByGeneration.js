export const POKEMON_GENERATION_IDS = POKEMON_GENERATIONS.map((generation) => generation.id);

export const getPokemonGenerationsByIds = (ids = []) => {
  const selectedIds = new Set(ids);
  return POKEMON_GENERATIONS.filter((generation) => selectedIds.has(generation.id));
};

export const getPokemonWordPool = (ids = []) => {
  const uniqueNames = new Set();
  getPokemonGenerationsByIds(ids).forEach((generation) => {
    generation.playableNames.forEach((name) => uniqueNames.add(name));
  });
  return [...uniqueNames];
};
